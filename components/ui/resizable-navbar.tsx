"use client";
import { cn } from "@/lib/utils";
import { IconX } from "@tabler/icons-react";
import {
  motion,
  AnimatePresence,
  useScroll,
  useMotionValueEvent,
} from "motion/react";
import Image from "next/image";
import React, { useRef, useState } from "react";

interface NavbarProps {
  children: React.ReactNode;
  className?: string;
}

interface NavBodyProps {
  children: React.ReactNode;
  className?: string;
  visible?: boolean;
}

interface NavItemsProps {
  items: {
    name: string;
    link: string;
  }[];
  className?: string;
  onItemClick?: () => void;
}

interface MobileNavProps {
  children: React.ReactNode;
  className?: string;
  visible?: boolean;
}

interface MobileNavHeaderProps {
  children: React.ReactNode;
  className?: string;
}

export const Navbar = ({ children, className }: NavbarProps) => {
  const ref = useRef<HTMLDivElement>(null);
  const { scrollY } = useScroll({
    target: ref,
    offset: ["start start", "end start"],
  });
  const [visible, setVisible] = useState<boolean>(false);

  useMotionValueEvent(scrollY, "change", (latest) => {
    if (latest > 100) {
      setVisible(true);
    } else {
      setVisible(false);
    }
  });

  return (
    <motion.div
      ref={ref}
      className={cn("fixed inset-x-0 top-10 z-99 w-full", className)}
    >
      {React.Children.map(children, (child) =>
        React.isValidElement(child)
          ? React.cloneElement(
              child as React.ReactElement<{ visible?: boolean }>,
              { visible }
            )
          : child
      )}
    </motion.div>
  );
};

export const NavBody = ({ children, className, visible }: NavBodyProps) => {
  return (
    <motion.div
      animate={{
        backdropFilter: visible ? "blur(10px)" : "none",
        boxShadow: "none",
        width: visible ? "40%" : "100%",
        y: visible ? 20 : 0,
      }}
      transition={{
        type: "spring",
        stiffness: 200,
        damping: 50,
      }}
      style={{
        minWidth: "950px",
        borderRadius: "25px",
      }}
      className={cn(
        "relative z-99 mx-auto hidden w-full max-w-6xl flex-row items-center justify-between self-start px-5 h-[70px] lg:flex  ",
        className
      )}
    >
      {children}
    </motion.div>
  );
};

export const NavItems = ({ items, className, onItemClick }: NavItemsProps) => {
  const [hovered, setHovered] = useState<number | null>(null);

  return (
    <motion.ul
      onMouseLeave={() => setHovered(null)}
      className={cn(
        "relative flex items-center rounded-2xl px-5 py-4 text-base font-medium text-white transition duration-200 hover:text-zinc-800",
        className
      )}
    >
      {/* Fundo permanente com cor + blur */}
      <div className="absolute inset-0 bg-zinc-800/40 backdrop-blur-md rounded-2xl z-0 pointer-events-none" />

      {items.map((item, idx) => (
        <li key={`link-${idx}`} className="relative z-10">
          <a
            onMouseEnter={() => setHovered(idx)}
            onClick={onItemClick}
            className="relative px-4 py-2 text-white/80 hover:text-white"
            href={item.link}
          >
            {/* Hover do item */}
            {hovered === idx && (
              <motion.div
                layoutId="hovered"
                className="absolute inset-0 h-full w-full rounded-[15px] bg-black/10 backdrop-blur-sm z-0"
              />
            )}
            <span className="relative z-20">{item.name}</span>
          </a>
        </li>
      ))}
    </motion.ul>
  );
};

